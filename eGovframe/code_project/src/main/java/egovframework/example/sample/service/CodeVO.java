package egovframework.example.sample.service;

public class CodeVO {
	
	/**일련번호*/
	private int code;
	
	/**분류*/
	private int gid;
	
	/**코드명*/
	private String name;

	public int getCode() {
		return code;
	}

	public void setCode(int code) {
		this.code = code;
	}

	public int getGid() {
		return gid;
	}

	public void setGid(int gid) {
		this.gid = gid;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
}
